    private void listjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listjButton1ActionPerformed
        BufferedReader in = null;
        
       
        
        try {
             String[] splited = null;
            in = new BufferedReader(new FileReader("books.txt"));
            String read;
            ArrayList<String> answer = new ArrayList<String>();
            
            while((read = in.readLine()) !=null){
                splited = read.split(",");
                
                if(authornamejTextField1.getText().equals(splited[3])){
                    answer.add(splited[1]+" : "+splited[8]);
                    
                }
            }
            String name="";
            
            for(String string : answer){
                name = name+string;
                name = name+"\n";
            }
            jTextArea1.setText(name);
            in.close();
            
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ListAuthor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ListAuthor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_listjButton1ActionPerformed

