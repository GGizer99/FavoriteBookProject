    private void deletejButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletejButton1ActionPerformed
       File inputFile = new File("books.txt");
       File tempFile = new File("myTempFile");
       
       BufferedReader reader = null;
       BufferedWriter writer = null;
       
        try {
            reader= new BufferedReader(new FileReader(inputFile));
            writer = new BufferedWriter(new FileWriter(tempFile));
            
            String lineToRemove = idjTextField1.getText();
            String currentLine;
            
            while((currentLine = reader.readLine())!= null){
                String trimmedLine = currentLine.trim();
                if(trimmedLine.startsWith(lineToRemove))continue;
                writer.write(currentLine + System.getProperty("line.separator"));
            }
            writer.close();
            reader.close();
            
            if(inputFile.delete()){
                System.out.println(" deleted");
                JOptionPane.showMessageDialog(null,"Deleted ");
            }
            else{
                System.out.println("failed");
            }
            tempFile.renameTo(inputFile);
            
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DeleteBook.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DeleteBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
       
        
        
    }//GEN-LAST:event_deletejButton1ActionPerformed

