    private void listjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listjButton1ActionPerformed
        BufferedReader in = null;
        
        
        
        try {
            String[] splited = null;
            in = new BufferedReader(new FileReader("books.txt"));
            String read;
            ArrayList<String> answers = new ArrayList<String>();
            while((read = in.readLine()) != null){
                splited = read.split(",");
                if(categoryjTextField1.getText().equals(splited[2])) {
                    	            	
                    answers.add(splited[5]+","+splited[1]); 
                }
            }
            Collections.sort(answers);
            String name ="";
            
            for(String string : answers){
                name = name+string.substring(string.indexOf(",")+1);
                name = name+"\n";
            }
            bookstitlejTextArea1.setText(name);
            in.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ListbyCategorySorted.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ListbyCategorySorted.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_listjButton1ActionPerformed

