    private void listbooksjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listbooksjButton1ActionPerformed
        BufferedReader in = null;
        
        
        
        try {
            String splited[] = null;
            in = new BufferedReader(new FileReader("books.txt"));
            String read=null;
            ArrayList<String> answer = new ArrayList<String>();
            
            while((read = in.readLine())!=null){
                splited = read.split(",");
                
                if(categoryjTextField1.getText().equals(splited[2])){
                    answer.add(splited[1]);
                }
                
            }
            String name = "";
            
            for(String string : answer){
                
                name = name+string;
                name = name+"\n";
            }
            jTextArea1.setText(name);
            in.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ListbyCategory.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ListbyCategory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_listbooksjButton1ActionPerformed

